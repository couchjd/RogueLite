Map::Map()
{
    std::memset(&m_tiles, ' ', sizeof(char) * MAP_HEIGHT * MAP_WIDTH);
}

Map::~Map()
{

}

void Map::init()
{
    for(int y = 0; y < MAP_HEIGHT; ++y)
    {
        for(int x = 0; x < MAP_WIDTH; ++x)
        {
            m_tiles[y][x] = tile_map[y][x];
        }
    }
}

void Map::draw()
{
    for(int y = 0; y < MAP_HEIGHT; ++y)
    {
        printw("%s\n", m_tiles[y]);
    }
}