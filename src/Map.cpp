void Map::init()
{
    for(int y = 0; y < MAP_HEIGHT; ++y)
    {
        for(int x = 0; x < MAP_WIDTH; ++x)
        {
            m_tiles[y][x] = new Tile(tile_map[y][x]);
        }
    }
}

void Map::draw()
{
    for(int y = 0; y < MAP_HEIGHT; ++y)
    {
        for(int x = 0; x < MAP_WIDTH; ++x)
        {
            mvprintw(x, y, "%s", m_tiles[y][x]->getCharacter());
            //printw("%s", m_tiles[y][x]->getCharacter());
        }
        printw("\n");
    }
}